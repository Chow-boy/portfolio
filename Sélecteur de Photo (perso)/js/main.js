"use strict";

/*************************************************************************************************/
/* ****************************************** DONNEES ****************************************** */
/*************************************************************************************************/
var photo = document.querySelectorAll('.selected');
var total = document.querySelector("#total span");


/*************************************************************************************************/
/* ***************************************** FONCTIONS ***************************************** */
/*************************************************************************************************/
function onClickPhoto(){

  this.classList.toggle('photo');
  var comptage = document.querySelectorAll('.photo');

  total.textContent = comptage.length;
}

/*************************************************************************************************/
/* ************************************** CODE PRINCIPAL *************************************** */
/*************************************************************************************************/


for (var i = 0; i < photo.length; i++) {
photo[i].addEventListener('click', onClickPhoto);
}
